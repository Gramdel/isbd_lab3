       ПР."ФАМИЛИЯ",
       ПР."ИМЯ",
       ПР."ОТЧЕСТВО",
       ВСЕ."ИД",
       ВСЕ."ФАМИЛИЯ",
       ВСЕ."ИМЯ",
       ВСЕ."ОТЧЕСТВО"
from "Н_ЛЮДИ" ПР
         left join "Н_УЧЕНИКИ" У1 on ПР."ИД" = У1."ЧЛВК_ИД"
         join "Н_ЛЮДИ" ВСЕ on ПР."ОТЧЕСТВО" = ВСЕ."ОТЧЕСТВО"
where У1."ИД" is null